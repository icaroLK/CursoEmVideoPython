n1 = int(input('Insira um número: '))
n2 = int(input('Insira um número: '))
op = int(input('''Escolha a operação a ser realizada: 
[1] Soma
[2] Subtração
[3] Multiplicação
[4] Divisão
R: '''))

if op == 1:
    print(n1 + n2)
if op == 2:
    print(n1 - n2)
if op == 3:
    print(n1 * n2)
if op == 1:
    print(n1 / n2)

