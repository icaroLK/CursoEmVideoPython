n = int(input('Digite um número: '))
print('Analisando o valor {0} \nO dobro de {0} é {1} \nO triplo de {0} é {2} \n' .format(n, n*2, n*3), end='')
print('A raiz quadrada de {0} é aproximadamente {1:.2f}' .format(n, n**(1/2)))
